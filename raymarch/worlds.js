function world_brighten(ray, color) {
	ray.color[0] += color[0];
	ray.color[1] += color[1];
	ray.color[2] += color[2];
	ray.color[3] += color[3] ?? 1;
}

function world_spherize(ray, sphereR) {
	ray.dPos[1] += (ray.localDist / sphereR);
	ray.dPos = normalize(ray.dPos);
}


