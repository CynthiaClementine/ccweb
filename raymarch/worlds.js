function world_loopRay(ray, width) {
	var rPos = ray.pos;
	var ww = ray.world.width ?? width;
	rPos[0] = modulate(rPos[0], ww);
	rPos[1] = modulate(rPos[1], ww);
	rPos[2] = modulate(rPos[2], ww);
}

